export function getStoryById(id: number) {
  return DUMMY_STORIES.find((story) => story.id === id);
}

// 이전/다음 스토리 ID 가져오기
export function getAdjacentStoryIds(id: number) {
  const currentIndex = DUMMY_STORIES.findIndex((story) => story.id === id);
  
  if (currentIndex === -1) {
    return { prevId: null, nextId: null };
  }

  const prevId = currentIndex > 0 ? DUMMY_STORIES[currentIndex - 1].id : null;
  const nextId = currentIndex < DUMMY_STORIES.length - 1 ? DUMMY_STORIES[currentIndex + 1].id : null;

  return { prevId, nextId };
}

